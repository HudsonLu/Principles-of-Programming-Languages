from math import *
print(e, pi)       # Useful constants.
print(log(e))      # 1.0 (log base 'e')
print(log10(100))  # 2.0 (log base 10)
print(log2(8))     # 3.0 (log base 2)
print(sin(pi / 2)) # 1.0
print(exp(1))      # 'e'
print(ceil(-1.2))    # -1
print(floor(-1.2)) # -2
print(fabs(-1.2))  # 1.2







